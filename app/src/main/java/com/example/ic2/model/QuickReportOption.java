package com.example.ic2.model;

public class QuickReportOption {
    private String date;
    private String time;
    private String location;
    private String department;
    private String typeOfIncident;
    private String projectReference;
    private String describeOfIssue;
    private String doAboutId;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }

    public String getTypeOfIncident() {
        return typeOfIncident;
    }

    public void setTypeOfIncident(String typeOfIncident) {
        this.typeOfIncident = typeOfIncident;
    }

    public String getProjectReference() {
        return projectReference;
    }

    public void setProjectReference(String projectReference) {
        this.projectReference = projectReference;
    }

    public String getDescribeOfIssue() {
        return describeOfIssue;
    }

    public void setDescribeOfIssue(String describeOfIssue) {
        this.describeOfIssue = describeOfIssue;
    }

    public String getDoAboutId() {
        return doAboutId;
    }

    public void setDoAboutId(String doAboutId) {
        this.doAboutId = doAboutId;
    }
}
